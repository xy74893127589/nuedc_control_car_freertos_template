static void reset_controller_transfer(void)
{
    DL_I2C_disableController(s_i2c);
    DL_I2C_resetControllerTransfer(s_i2c);
    DL_I2C_enableController(s_i2c);
}

static bool wait_until_idle(void)
{
    uint32_t timeout = SSD1306_I2C_TIMEOUT;

    while ((DL_I2C_getControllerStatus(s_i2c) & DL_I2C_CONTROLLER_STATUS_BUSY) != 0u) {
        if (--timeout == 0u) {
            return false;
        }
    }

    timeout = SSD1306_I2C_TIMEOUT;
    while ((DL_I2C_getControllerStatus(s_i2c) & DL_I2C_CONTROLLER_STATUS_IDLE) == 0u) {
        if (--timeout == 0u) {
            return false;
        }
    }

    return (DL_I2C_getControllerStatus(s_i2c) & DL_I2C_CONTROLLER_STATUS_ERROR) == 0u;
}

static bool write_packet(const uint8_t *packet, uint16_t length)
{
    uint32_t delay_cycles_for_errata;

    if ((s_i2c == NULL) || (packet == NULL) || (length == 0u)) {
        return false;
    }

    if (!wait_until_idle()) {
        reset_controller_transfer();
        return false;
    }

    DL_I2C_flushControllerTXFIFO(s_i2c);
    if (DL_I2C_fillControllerTXFIFO(s_i2c, packet, length) != length) {
        return false;
    }

    DL_I2C_startControllerTransfer(s_i2c, s_address,
                                   DL_I2C_CONTROLLER_DIRECTION_TX, length);

    delay_cycles_for_errata = 30u;
    while (delay_cycles_for_errata-- > 0u) {
        __NOP();
    }

    if (!wait_until_idle()) {
        reset_controller_transfer();
        return false;
    }

    return true;
}

static bool write_command_list(const uint8_t *commands, uint8_t length)
{
    uint8_t packet[8];
    uint8_t offset = 0u;

    while (offset < length) {
        uint8_t chunk = (uint8_t)(length - offset);
        if (chunk > 7u) {
            chunk = 7u;
        }

        packet[0] = SSD1306_CONTROL_COMMAND;
        for (uint8_t i = 0u; i < chunk; i++) {
            packet[i + 1u] = commands[offset + i];
        }

        if (!write_packet(packet, (uint16_t)(chunk + 1u))) {
            return false;
        }
        offset = (uint8_t)(offset + chunk);
    }

    return true;
}

static bool write_data_list(const uint8_t *data, uint8_t length)
{
    uint8_t packet[8];
    uint8_t offset = 0u;

    while (offset < length) {
        uint8_t chunk = (uint8_t)(length - offset);
        if (chunk > 7u) {
            chunk = 7u;
        }

        packet[0] = SSD1306_CONTROL_DATA;
        for (uint8_t i = 0u; i < chunk; i++) {
            packet[i + 1u] = data[offset + i];
        }

        if (!write_packet(packet, (uint16_t)(chunk + 1u))) {
            return false;
        }
        offset = (uint8_t)(offset + chunk);
    }

    return true;
}

static bool set_cursor(uint8_t row, uint8_t col)
{
    uint8_t column = (uint8_t)(col * SSD1306_CELL_WIDTH);
    uint8_t commands[] = {
        (uint8_t)(0xB0u + row),
        (uint8_t)(0x00u + (column & 0x0Fu)),
        (uint8_t)(0x10u + ((column >> 4u) & 0x0Fu)),
    };

    if ((row >= SSD1306_TEXT_ROWS) || (col >= SSD1306_TEXT_COLUMNS)) {
        return false;
    }

    return write_command_list(commands, sizeof(commands));
}

static uint8_t normalize_char(char c)
{
    if ((c < ' ') || (c > '~')) {
        return (uint8_t)('?' - ' ');
    }
    return (uint8_t)(c - ' ');
}

static bool write_char(char c)
{
    uint8_t glyph[SSD1306_CELL_WIDTH];
    const uint8_t *font = s_font5x7[normalize_char(c)];

    for (uint8_t i = 0u; i < SSD1306_FONT_WIDTH; i++) {
        glyph[i] = font[i];
    }
    glyph[SSD1306_FONT_WIDTH] = 0x00u;

    return write_data_list(glyph, sizeof(glyph));
}

bool SSD1306_MSPM0_Init(I2C_Regs *i2c, uint8_t address)
{
    static const uint8_t init_commands[] = {
        0xAE, 0x20, 0x02, 0xB0, 0xC8, 0x00, 0x10, 0x40,
        0x81, 0x7F, 0xA1, 0xA6, 0xA8, 0x3F, 0xA4, 0xD3,
        0x00, 0xD5, 0x80, 0xD9, 0xF1, 0xDA, 0x12, 0xDB,
        0x40, 0x8D, 0x14, 0xAF
    };

    s_i2c = i2c;
    s_address = address;
    reset_controller_transfer();

    return write_command_list(init_commands, sizeof(init_commands)) &&
           SSD1306_MSPM0_Clear();
}

bool SSD1306_MSPM0_Clear(void)
{
    uint8_t zeros[7] = {0};

    for (uint8_t row = 0u; row < SSD1306_PAGE_COUNT; row++) {
        uint8_t commands[] = {(uint8_t)(0xB0u + row), 0x00, 0x10};

        if (!write_command_list(commands, sizeof(commands))) {
            return false;
        }

        for (uint8_t col = 0u; col < SSD1306_WIDTH; col += sizeof(zeros)) {
            uint8_t count = (uint8_t)(SSD1306_WIDTH - col);
            if (count > sizeof(zeros)) {
                count = sizeof(zeros);
            }
            if (!write_data_list(zeros, count)) {
                return false;
            }
        }
    }

    return true;
}

bool SSD1306_MSPM0_WriteString(uint8_t row, uint8_t col, const char *text)
{
    uint8_t written = 0u;

    if ((text == NULL) || !set_cursor(row, col)) {
        return false;
    }

    while ((text[written] != '\0') &&
           ((col + written) < SSD1306_TEXT_COLUMNS)) {
        if (!write_char(text[written])) {
            return false;
        }
        written++;
    }

    return true;
}

bool SSD1306_MSPM0_WriteStringPadded(uint8_t row, uint8_t col,
                                     const char *text)
{
    uint8_t written = 0u;

    if ((text == NULL) || !set_cursor(row, col)) {
        return false;
    }

    while ((text[written] != '\0') &&
           ((col + written) < SSD1306_TEXT_COLUMNS)) {
        if (!write_char(text[written])) {
            return false;
        }
        written++;
    }

    while ((col + written) < SSD1306_TEXT_COLUMNS) {
        if (!write_char(' ')) {
            return false;
        }
        written++;
    }

    return true;
}
